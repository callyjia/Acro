/**
 * Formats number with dot separator: 2000 → 2.000
 */
fun formatWithDots(input: String): String {
    val digits = input.replace(".", "").filter { it.isDigit() }
    if (digits.isEmpty()) return ""
    return digits.reversed()
        .chunked(3)
        .joinToString(".")
        .reversed()
}

/**
 * Number input field with auto-formatting and error display
 * Strips non-digits, formats with dot separator
 * Shows red error message when input is invalid
 */
@Composable
fun ProductNumberField(
    label: String,
    value: String,
    onValueChange: (String) -> Unit,
    placeholder: String,
    isError: Boolean = false,
    errorMessage: String = ""
) {
    Column(verticalArrangement = Arrangement.spacedBy(4.dp)) {
        Text(
            text = label,
            fontSize = 12.sp,
            fontWeight = FontWeight.Bold,
            color = if (isError) MaterialTheme.colorScheme.error
            else MaterialTheme.colorScheme.onSurface.copy(alpha = 0.7f)
        )
        OutlinedTextField(
            value = value,
            onValueChange = { raw ->
                // Only allow digits, auto-format with dots
                val formatted = formatWithDots(raw)
                onValueChange(formatted)
            },
            modifier = Modifier.fillMaxWidth(),
            placeholder = {
                Text(
                    placeholder,
                    color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.3f)
                )
            },
            shape = RoundedCornerShape(8.dp),
            singleLine = true,
            isError = isError,
            keyboardOptions = KeyboardOptions(keyboardType = KeyboardType.Number),
            colors = OutlinedTextFieldDefaults.colors(
                focusedBorderColor = if (isError) MaterialTheme.colorScheme.error
                else MaterialTheme.colorScheme.primary,
                unfocusedBorderColor = if (isError) MaterialTheme.colorScheme.error
                else MaterialTheme.colorScheme.outline,
                focusedTextColor = MaterialTheme.colorScheme.onSurface,
                unfocusedTextColor = MaterialTheme.colorScheme.onSurface,
                cursorColor = MaterialTheme.colorScheme.primary,
                errorBorderColor = MaterialTheme.colorScheme.error
            )
        )
        // Error message
        if (isError && errorMessage.isNotBlank()) {
            Text(
                text = errorMessage,
                fontSize = 11.sp,
                color = MaterialTheme.colorScheme.error
            )
        }
    }
}
